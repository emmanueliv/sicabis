package com.issste.sicabis.DTO;

/**
 *
 * @author kriosoft
 */
public class ReporteGeneralDTO {
    
    private String rcb;
    private String procedimiento;
    private String fallo;
    private String contrato;
    private String convenio;
    private String ordenSuministro;
    private String remision;

    public String getRcb() {
        return rcb;
    }

    public void setRcb(String rcb) {
        this.rcb = rcb;
    }

    public String getProcedimiento() {
        return procedimiento;
    }

    public void setProcedimiento(String procedimiento) {
        this.procedimiento = procedimiento;
    }

    public String getFallo() {
        return fallo;
    }

    public void setFallo(String fallo) {
        this.fallo = fallo;
    }

    public String getContrato() {
        return contrato;
    }

    public void setContrato(String contrato) {
        this.contrato = contrato;
    }

    public String getConvenio() {
        return convenio;
    }

    public void setConvenio(String convenio) {
        this.convenio = convenio;
    }

    public String getOrdenSuministro() {
        return ordenSuministro;
    }

    public void setOrdenSuministro(String ordenSuministro) {
        this.ordenSuministro = ordenSuministro;
    }

    public String getRemision() {
        return remision;
    }

    public void setRemision(String remision) {
        this.remision = remision;
    }
    
    
}
